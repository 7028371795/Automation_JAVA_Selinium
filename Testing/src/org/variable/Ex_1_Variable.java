package org.variable;

public class Ex_1_Variable {

	public void main(String args[]) {
		
		int a=10;
		byte b=2;
		boolean c=true;
		double d=12;
		float f=12.5f;
		
		System.err.println(a);
		System.err.println(b);
		System.err.println(c);
		System.err.println(d);
		System.err.println(f);

	}
}
